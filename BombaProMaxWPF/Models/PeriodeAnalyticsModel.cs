namespace BombaProMaxWPF.Models;

/// <summary>
/// Analytics model for a Periode, with grouped totals by product, reservoir, and pump.
/// </summary>
public class PeriodeAnalyticsModel
{
    // ???????????????????????????????????????????????????????????????????????
    // HEADER INFO
    // ???????????????????????????????????????????????????????????????????????
    
    public int PeriodeID { get; set; }
    public DateTime DateDebut { get; set; }
    public DateTime DateFin { get; set; }
    public string? EmployeNom { get; set; }
    
    // ???????????????????????????????????????????????????????????????????????
    // FINANCIAL ANALYTICS
    // ???????????????????????????????????????????????????????????????????????
    
    public decimal TPE { get; set; }
    public decimal Especes { get; set; }
    public decimal TotalPaiements => TPE + Especes;
    public decimal Recette { get; set; }
    public decimal Ecart => TotalPaiements - Recette;
    public decimal EcartPercent => Recette != 0 ? Math.Round((Ecart / Recette) * 100, 2) : 0;
    public bool HasDeficit => Ecart < -0.01m;
    public bool HasSurplus => Ecart > 0.01m;
    
    // ???????????????????????????????????????????????????????????????????????
    // QUANTITY TOTALS
    // ???????????????????????????????????????????????????????????????????????
    
    public decimal TotalQuantite { get; set; }
    public decimal TotalEcartCompteurs { get; set; }
    
    // ???????????????????????????????????????????????????????????????????????
    // MARGE (PROFIT) ANALYTICS
    // ???????????????????????????????????????????????????????????????????????
    
    /// <summary>
    /// Total cost of goods sold (from StockLot.PrixAchat via FIFO)
    /// </summary>
    public decimal TotalCoutAchat { get; set; }
    
    /// <summary>
    /// Total sales revenue (Recette)
    /// </summary>
    public decimal TotalVente => Recette;
    
    /// <summary>
    /// Gross margin (Vente - CoutAchat)
    /// </summary>
    public decimal TotalMarge => TotalVente - TotalCoutAchat;
    
    /// <summary>
    /// Margin percentage
    /// </summary>
    public decimal MargePercent => TotalVente != 0 ? Math.Round((TotalMarge / TotalVente) * 100, 2) : 0;
    
    // ???????????????????????????????????????????????????????????????????????
    // GROUPED TOTALS
    // ???????????????????????????????????????????????????????????????????????
    
    public List<ProductTotalModel> TotauxParProduit { get; set; } = [];
    public List<ReservoirTotalModel> TotauxParReservoir { get; set; } = [];
    public List<PompeTotalModel> DetailsParPompe { get; set; } = [];
    
    /// <summary>
    /// Stock consumption breakdown showing which lots were consumed at what price
    /// </summary>
    public List<StockConsumptionModel> ConsommationsStock { get; set; } = [];
}

/// <summary>
/// Total for a specific product (fuel type)
/// </summary>
public class ProductTotalModel
{
    public int? ProduitID { get; set; }
    public string ProduitNom { get; set; } = "N/A";
    public string? ProduitCouleur { get; set; }
    public decimal Quantite { get; set; }
    public decimal Montant { get; set; }
    public decimal PrixMoyen => Quantite != 0 ? Math.Round(Montant / Quantite, 2) : 0;
    public int NombrePompes { get; set; }
    
    // Marge info
    public decimal CoutAchat { get; set; }
    public decimal Marge => Montant - CoutAchat;
    public decimal MargePercent => Montant != 0 ? Math.Round((Marge / Montant) * 100, 2) : 0;
}

/// <summary>
/// Total for a specific reservoir
/// </summary>
public class ReservoirTotalModel
{
    public int? ReservoirID { get; set; }
    public string ReservoirNumero { get; set; } = "N/A";
    public string? ProduitNom { get; set; }
    public decimal QuantiteConsommee { get; set; }
    public decimal? NiveauActuel { get; set; }
    public decimal? Capacite { get; set; }
    public decimal? PourcentageRemplissage => 
        Capacite.HasValue && Capacite > 0 && NiveauActuel.HasValue 
            ? Math.Round((NiveauActuel.Value / Capacite.Value) * 100, 1) 
            : null;
    public int NombrePompes { get; set; }
    public decimal Montant { get; set; }
}

/// <summary>
/// Details for a specific pump reading
/// </summary>
public class PompeTotalModel
{
    public int? PompeID { get; set; }
    public string PompeNumero { get; set; } = "N/A";
    public string? ReservoirNumero { get; set; }
    public string? ProduitNom { get; set; }
    
    public decimal CompteurElecDebut { get; set; }
    public decimal CompteurElecFin { get; set; }
    public decimal CompteurMecaDebut { get; set; }
    public decimal CompteurMecaFin { get; set; }
    
    public decimal QuantiteElec => CompteurElecFin - CompteurElecDebut;
    public decimal QuantiteMeca => CompteurMecaFin - CompteurMecaDebut;
    public decimal EcartCompteurs => Math.Abs(QuantiteElec - QuantiteMeca);
    
    public bool HasEcartSignificatif
    {
        get
        {
            if (EcartCompteurs < 0.01m) return false;
            var maxQte = Math.Max(Math.Abs(QuantiteElec), Math.Abs(QuantiteMeca));
            if (maxQte == 0) return false;
            var percent = (EcartCompteurs / maxQte) * 100;
            return EcartCompteurs > 1 || percent > 1;
        }
    }
    
    public decimal QuantiteVendue { get; set; }
    public decimal PrixUnitaire { get; set; }
    public decimal Montant { get; set; }
}

/// <summary>
/// Stock consumption detail for margin analysis
/// Shows which StockLot was consumed, at what PrixAchat, and the resulting margin
/// </summary>
public class StockConsumptionModel
{
    public int StockLotID { get; set; }
    public string? ProduitNom { get; set; }
    public string? ReservoirNumero { get; set; }
    
    /// <summary>
    /// Purchase price per liter from StockLot
    /// </summary>
    public decimal PrixAchat { get; set; }
    
    /// <summary>
    /// Selling price per liter
    /// </summary>
    public decimal PrixVente { get; set; }
    
    /// <summary>
    /// Quantity consumed from this lot
    /// </summary>
    public decimal QuantiteConsommee { get; set; }
    
    /// <summary>
    /// Cost of goods sold (Qte * PrixAchat)
    /// </summary>
    public decimal CoutAchat => QuantiteConsommee * PrixAchat;
    
    /// <summary>
    /// Revenue from this consumption (Qte * PrixVente)
    /// </summary>
    public decimal Vente => QuantiteConsommee * PrixVente;
    
    /// <summary>
    /// Gross margin for this consumption
    /// </summary>
    public decimal Marge => Vente - CoutAchat;
    
    /// <summary>
    /// Margin per liter
    /// </summary>
    public decimal MargeParLitre => PrixVente - PrixAchat;
    
    /// <summary>
    /// Margin percentage
    /// </summary>
    public decimal MargePercent => Vente != 0 ? Math.Round((Marge / Vente) * 100, 2) : 0;
}
